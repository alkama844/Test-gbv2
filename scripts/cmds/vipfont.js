const base = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ".split("");

module.exports = {
	config: {
		name: "vipfont",
		version: "4.0",
		author: "NAFIJ_PRO_✅",
		countDown: 2,
		role: 0,
		shortDescription: "VIP font stylizer",
		longDescription: "Convert text into various VIP fonts (25+ safe styles)",
		category: "text",
		guide: "{pn} <style> <text>\nType {pn} to see all fonts"
	},

	onStart: async ({ event, args, api }) => {
		const uid = event.senderID;
		const vipData = loadVIPData();

		if (!vipData[uid]) {
			return api.sendMessage("⛔ 𝗩𝗜𝗣 𝗢𝗡𝗟𝗬 ⛔\n 𝗔𝗦𝗞 𝗙𝗥𝗢𝗠 𝗔𝗗𝗠𝗜𝗡 𝗢𝗥 𝗕𝗢𝗧 𝗔𝗗𝗠𝗜𝗡 .", event.threadID);
		}

		if (args.length === 0) {
			const list = Object.keys(fontStyles).map(f => `- ${f}`).join("\n");
			return api.sendMessage(`✅ VIP Fonts Unlocked:\n\n${list}\n\nExample:\n.font2 bold Nafij\n.font2 round GoatBot`, event.threadID);
		}

		const style = args[0].toLowerCase();
		const content = args.slice(1).join(" ");

		if (!fontStyles[style]) {
			const list = Object.keys(fontStyles).map(f => `- ${f}`).join("\n");
			return api.sendMessage(`⚠️ Unknown font style "${style}".\nAvailable:\n${list}`, event.threadID);
		}

		if (!content) return api.sendMessage("❗ Please provide text to convert.\nExample: .font2 medieval Nafij", event.threadID);

		const map = fontStyles[style];
		let result = "";

		for (const ch of content) {
			const i = base.indexOf(ch);
			result += i >= 0 ? map[i] : ch;
		}

		api.sendMessage(`【${style}】\n${result}`, event.threadID);
	}
};